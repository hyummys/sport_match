/** 스포츠 이름에 해당하는 레벨 설명 배열 반환 */
export const getLevelDescriptions = (sportName: string): string[] =>
  SPORT_LEVEL_DESCRIPTIONS[sportName] || DEFAULT_LEVEL_DESCRIPTIONS;

/** 특정 레벨의 설명 반환 */
export const getLevelDescription = (sportName: string, level: SkillLevel): string => {
  const descriptions = getLevelDescriptions(sportName);
  return descriptions[Math.min(Math.max(level, 0), 10)] || '';
};

