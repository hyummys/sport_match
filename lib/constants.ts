/** 레벨 표시 텍스트 (예: "Lv.5") */
export const formatSkillLevel = (level: SkillLevel): string => `Lv.${level}`;

/** 레벨 범위 표시 (예: "Lv.3 ~ Lv.7") */
export const formatSkillRange = (min: SkillLevel, max: SkillLevel): string =>
  min === max ? formatSkillLevel(min) : `Lv.${min} ~ Lv.${max}`;

